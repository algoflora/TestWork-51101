class Article {
  constructor({id, title, description, content, image, author, created, updated}) {
    this.id          = id;
    this.title       = title;
    this.description = description;
    this.content     = content;
    this.image       = image;
    this.author      = author;
    this.created     = created;
    this.updated     = updated;
  }
}
